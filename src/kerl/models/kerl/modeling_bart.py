class BartEncoderModel(nn.Module):
    def __init__(
            self, vocab_size: int = 50265, dropout_prob: float = 0.1,
            pooler_type: str = "cls", num_heads: int = 8, out_dim: int = 1024):
        """
        :param vocab_size: vocab size of the model
        :param dropout_prob: dropout probability for attention pooling
        :param pooler_type: type of pooling to use, cls, avg, attention
        :param num_heads: number of heads for multi-head attention in attention pooling
        :param out_dim: output dimension for the encoder and input dimension for the decoder and cross-attention layers
        """
        super().__init__()

        if pooler_type not in ["cls", "avg", "attention"]:
            raise ValueError(f"pooler_type must be one of cls, avg, attention. you passed {pooler_type}")

        self.model = BartEncoderWrapper.from_pretrained("facebook/bart-base")
        self.model.resize_token_embeddings(vocab_size)
        logger.debug(f"BartEncoderModel: vocab_size: {vocab_size}")

        # encoder
        self.pooler_type = pooler_type
        self.dropout = nn.Dropout(dropout_prob)
        self.h_dim = self.model.config.d_model
        self.out_dim = out_dim
        self.n_heads = num_heads
        # this pooler is used to pool the final hidden states of the cross-attention layer
        self.pooler = OutputPool(self.pooler_type, self.n_heads, self.h_dim, self.out_dim, dropout_prob)

    @property
    def last_hidden_size(self):
        return self.model.config.hidden_size

    @property
    def ffn_dim_size(self):
        return self.model.config.d_model

    def forward(self, input_ids, attention_mask, mode="rec"):
        hidden_states = self.model.encoder(input_ids, attention_mask=attention_mask)
        return hidden_states.last_hidden_state

    def get_pooled_output(self, hidden_states, attention_mask=None):
        return self.pooler(hidden_states, attention_mask)

    def get_cross_attention(self, entity_rep, entity_mask, token_rep, token_mask):
        return self.cross_attention(entity_rep, entity_mask, token_rep, token_mask)


class OutputPool(nn.Module):
    def __init__(self, pooler_type: str = "cls", num_heads: int = 8, in_dim: int = 768, out_dim: int = 768,
                 dropout_prob: float = 0.1):
        super().__init__()

        self.pooler_type = pooler_type
        self.dropout = nn.Dropout(dropout_prob)
        self.in_dim = in_dim
        self.out_dim = out_dim
        if self.pooler_type == "attention":
            self.multi_head_attention = nn.MultiheadAttention(
                self.in_dim,
                num_heads,
                batch_first=True)
            self.attention_pooling_layer = AdditiveAttention(self.in_dim, self.in_dim)
        self.ffn = nn.Sequential(
            nn.Linear(self.in_dim, self.in_dim),
            nn.ReLU(),
            nn.Linear(self.in_dim, self.out_dim))

    def _init_weights(self):
        """ Initialize the weights """
        for module in self.ffn:
            if isinstance(module, nn.Linear):
                nn.init.xavier_uniform_(module.weight)
                nn.init.constant_(module.bias, 0.01)

    def forward(self, hidden_states, attention_mask):
        """
        Pooling layer for encoder
        :param hidden_states: last hidden states from encoder
        :param attention_mask: attention mask for encoder
        :return:
        """
        hidden_states = self.dropout(hidden_states)
        if self.pooler_type == "cls":
            hidden_states = self.cls_token_repr(hidden_states)
        elif self.pooler_type == "avg":
            hidden_states = self.avg_pooler(hidden_states, attention_mask)
        else:
            hidden_states = self.attention_pooling(hidden_states, attention_mask)
            
        hidden_states = self.ffn(hidden_states)
        return hidden_states

    @staticmethod
    def cls_token_repr(hidden_states):
        # original BART classification use eos token from decoder as cls token
        # here we use cls token from encoder
        return hidden_states[:, 0, :]

    @staticmethod
    def avg_pooler(hidden_states, attention_mask):
        input_mask_expanded = attention_mask.unsqueeze(-1).expand(hidden_states.size()).float()
        return torch.sum(hidden_states * input_mask_expanded, 1) / torch.clamp(input_mask_expanded.sum(1), min=1e-9)

    def attention_pooling(self, hidden_states, attention_mask):
        # batch_size, hidden_size
        # torch multi-head attention use True for mask which means ignore. However, the attention mask from
        # transformers use True for mask which means keep. So we need to reverse the mask
        torch_attention_mask = torch.logical_not(attention_mask)
        mh_attention, _ = self.multi_head_attention(hidden_states, hidden_states, hidden_states, torch_attention_mask)
        return self.attention_pooling_layer(mh_attention, attention_mask)


class BartDecoderModel(nn.Module):

    def __init__(self, vocab_size: int = 50265):
        """
        :param vocab_size: vocab size of the model
        """
        super().__init__()

        self.model = BartDecoderWrapper.from_pretrained("facebook/bart-base")
        self.model.resize_token_embeddings(vocab_size)
        logger.debug(f"BartDecoderModel: vocab_size: {vocab_size}", )

    def forward(
            self, encoder_outputs, encoder_attention_mask=None, decoder_input_ids=None, kg_hidden_states=None,
            kg_attention_mask=None, labels=None):
        if labels is None:
            # greedy decoding
            return self.get_decoder_outputs(
                decoder_input_ids=decoder_input_ids,
                decoder_attention_mask=decoder_input_ids.ne(self.model.config.pad_token_id).long(),
                encoder_hidden_states=encoder_outputs,
                encoder_attention_mask=encoder_attention_mask,
                kg_hidden_states=kg_hidden_states,
                kg_attention_mask=kg_attention_mask)

        # training/evaluation with labels
        # shift tokens left
        decoder_input_ids = shift_tokens_right(
            labels,
            self.model.config.pad_token_id,
            self.model.config.decoder_start_token_id)

        return self.get_decoder_outputs(
            decoder_input_ids=decoder_input_ids,
            decoder_attention_mask=decoder_input_ids.ne(self.model.config.pad_token_id).long(),
            encoder_hidden_states=encoder_outputs,
            encoder_attention_mask=encoder_attention_mask,
            kg_hidden_states=kg_hidden_states,
            kg_attention_mask=kg_attention_mask)

    def get_decoder_outputs(
            self,
            encoder_hidden_states,
            encoder_attention_mask,
            kg_hidden_states,
            kg_attention_mask,
            decoder_input_ids=None,
            decoder_attention_mask=None
    ):

        decoder_outputs = self.model.decoder(
            input_ids=decoder_input_ids,
            attention_mask=decoder_attention_mask,
            encoder_hidden_states=encoder_hidden_states,
            encoder_attention_mask=encoder_attention_mask,
            kg_hidden_states=kg_hidden_states,
            kg_attention_mask=kg_attention_mask,
            use_cache=False, )

        return Seq2SeqModelOutput(
            last_hidden_state=decoder_outputs.last_hidden_state,
            past_key_values=decoder_outputs.past_key_values,
            decoder_hidden_states=decoder_outputs.hidden_states,
            decoder_attentions=decoder_outputs.attentions,
            cross_attentions=decoder_outputs.cross_attentions,
            encoder_last_hidden_state=encoder_hidden_states, )
